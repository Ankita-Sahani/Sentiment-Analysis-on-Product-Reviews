"""# Task 2 - Data Cleaning

### Removing rows with empty or null review text
First, I will remove rows where the 'Text' column is empty or contains null values. Although the previous `isnull().sum()` showed 0 nulls for 'Text', it's good practice to ensure no empty strings are present either.
"""

df.isnull().sum()

initial_rows = df.shape[0]
df = df[df['Text'].astype(bool)]
df = df.dropna(subset=['Text'])
rows_after_null_empty = df.shape[0]
print(f"Removed {initial_rows - rows_after_null_empty} rows with empty or null review text.")
display(df.head())

"""### Removing duplicate reviews
Next, I will identify and remove duplicate review entries. Duplicate reviews can skew analysis, so it's important to ensure each review is unique.
"""

initial_rows = df.shape[0]
df.drop_duplicates(subset=['Text', 'Score', 'Summary'], inplace=True)
rows_after_duplicates = df.shape[0]
print(f"Removed {initial_rows - rows_after_duplicates} duplicate review entries.")
display(df.head())

"""### Keeping only relevant columns
Finally, I will keep only the 'Text' and 'Score' columns, as requested, to simplify the DataFrame for further analysis.
"""

df = df[['Text', 'Score']]
display(df.head())

"""# Task 3 - Sentiment Analysis"""

