"""### Sentiment Analysis using TextBlob
I will use `TextBlob` to calculate the sentiment polarity score for each review. The polarity score ranges from -1 (negative) to 1 (positive).
"""

from textblob import TextBlob

# Function to get polarity score
def get_polarity(text):
    return TextBlob(text).sentiment.polarity

# Apply the function to the 'Text' column to get polarity scores
df['Polarity'] = df['Text'].apply(get_polarity)
display(df.head())

"""### Labeling Reviews based on Polarity Score
Now, I will categorize each review based on its polarity score:
- Positive: polarity > 0
- Negative: polarity < 0
- Neutral: polarity = 0
"""

def get_sentiment(polarity):
    if polarity > 0:
        return 'Positive'
    elif polarity < 0:
        return 'Negative'
    else:
        return 'Neutral'

# Apply the function to the 'Polarity' column to get sentiment labels
df['Sentiment'] = df['Polarity'].apply(get_sentiment)
display(df.head())

"""### Sentiment Distribution

"""

sentiment_counts = df['Sentiment'].value_counts()
print(sentiment_counts)

"""# Task 4 - Visualization (Minimum 3 charts)

### Sentiment Distribution Visualization
Let's visualize the distribution of Positive, Negative, and Neutral sentiments using different chart types.

### Chart 1: Bar chart showing count of Positive, Negative, and Neutral reviews
"""

# Chart 1: Bar chart showing count of Positive, Negative, and Neutral reviews
plt.figure(figsize=(8, 6))
sns.barplot(x=sentiment_counts.index, y=sentiment_counts.values, palette='viridis')
plt.title('Count of Reviews by Sentiment')
plt.xlabel('Sentiment')
plt.ylabel('Number of Reviews')
plt.show()

"""### Chart 2: Pie chart showing the percentage distribution of sentiments"""

# Chart 2: Pie chart showing the percentage distribution of sentiments
plt.figure(figsize=(8, 8))
plt.pie(sentiment_counts.values, labels=sentiment_counts.index, autopct='%1.1f%%', startangle=140, colors=sns.color_palette('viridis'))
plt.title('Percentage Distribution of Sentiments')
plt.axis('equal') # Equal aspect ratio ensures that pie is drawn as a circle.
plt.show()

"""### Chart 3: Original Rating vs. TextBlob Sentiment Polarity
Let's explore the relationship between the user's explicit star rating (Score) and the calculated TextBlob sentiment polarity. This can help us understand how well TextBlob's sentiment aligns with the numerical ratings.
"""

# Group by original Score and calculate the mean polarity for each score
score_sentiment_mean = df.groupby('Score')['Polarity'].mean().reset_index()

plt.figure(figsize=(10, 6))
sns.barplot(x='Score', y='Polarity', data=score_sentiment_mean, palette='coolwarm')
plt.title('Average TextBlob Polarity by Original Review Score')
plt.xlabel('Original Review Score (1-5)')
plt.ylabel('Average Sentiment Polarity')
plt.ylim(-0.5, 0.8) # Set appropriate y-limits for polarity range
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.show()

